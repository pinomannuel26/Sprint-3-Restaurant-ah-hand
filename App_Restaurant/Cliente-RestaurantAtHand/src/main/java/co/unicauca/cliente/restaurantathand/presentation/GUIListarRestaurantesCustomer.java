    public void verMenu (){
        GUIRestauranteEscogido objRestaurantEscogido  = new GUIRestauranteEscogido(restauranteSeleccionado);
        dskEscritorio.add(objRestaurantEscogido);
        try {
                objRestaurantEscogido.setMaximum(true);
            } catch (PropertyVetoException ex) {
                Logger.getLogger(GUIRestauranteEscogido.class.getName()).log(Level.SEVERE, null, ex);
            }
        try {
            this.setMaximum(false);
        } catch (PropertyVetoException ex) {
            Logger.getLogger(GUIListarRestaurantesCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
            objRestaurantEscogido.show();
    
    }
